package com.k2.mobile.app.model.bean;    
  
public class ResMenuBean {

	private String menuChsName;		// 菜单名称
	private String menuCode;		// 菜单代码
	private String menuId;			// 菜单ID
	private String menuLevel;		// 菜单级别
	
	public String getMenuChsName() {
		return menuChsName;
	}
	public void setMenuChsName(String menuChsName) {
		this.menuChsName = menuChsName;
	}
	public String getMenuCode() {
		return menuCode;
	}
	public void setMenuCode(String menuCode) {
		this.menuCode = menuCode;
	}
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuLevel() {
		return menuLevel;
	}
	public void setMenuLevel(String menuLevel) {
		this.menuLevel = menuLevel;
	}
	
}
 